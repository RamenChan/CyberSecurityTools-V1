sayi1, sayi2, sayi3 = map(int, input("Lütfen boşluk ile ayırarak üç sayı giriniz: ").split())
print(f"Sayılar: {sayi1}, {sayi2}, {sayi3}")

toplam = sayi1 + sayi2 + sayi3
print(f"Sayıların toplamı: {toplam}")
carpim = sayi1 * sayi2 * sayi3
print(f"Sayıların çarpımı: {carpim}")
cikarma = sayi1 - sayi2 - sayi3
print(f"Sayıların çıkarımı: {cikarma}")
