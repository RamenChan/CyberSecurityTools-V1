# Dört sayı tanımlama
sayi1 = 10
sayi2 = 5
sayi3 = 20
sayi4 = 4

# Toplama işlemi
toplam = sayi1 + sayi2 + sayi3 + sayi4
print(f"Toplam: {toplam}")

# Çıkarma işlemi
cikarma = sayi1 - sayi2
print(f"Çıkarma: {cikarma}")

# Çarpma işlemi
carpma = sayi1 * sayi2
print(f"Çarpma: {carpma}")

# Bölme işlemi
if sayi2 != 0:
    bolme = sayi1 / sayi2
    print(f"Bölme: {bolme}")
else:
    print("Bölme hatası: Sıfıra bölme!")
